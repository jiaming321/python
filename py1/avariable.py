# 变量：变量名必须以 大小写字母、数字和下划线 _ 组成，且不能以数字开头。
# python中的变量类型不固定，故也称为动态语言，与之对应的为静态语言，是指在定义变量时须指定变量类型，且赋值类型与变量类型要匹配。

